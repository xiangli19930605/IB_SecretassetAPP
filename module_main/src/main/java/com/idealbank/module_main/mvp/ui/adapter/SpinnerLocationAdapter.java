public class SpinnerLocationAdapter extends BaseAdapter {
    private List<Location> mList;
    private Context mContext;

    public SpinnerLocationAdapter(Context pContext, List<Location> pList) {
        this.mContext = pContext;
        this.mList = pList;
    }

    @Override
    public int getCount() {
        return mList.size();
    }

    @Override
    public Object getItem(int position) {
        return mList.get(position);
    }

    @Override
    public long getItemId(int position) {
        return position;
    }

    /**
     * 下面是重要代码
     */
    @Override
    public View getView(int position, View convertView, ViewGroup parent) {
        LayoutInflater _LayoutInflater = LayoutInflater.from(mContext);
        convertView = _LayoutInflater.inflate(R.layout.spinner_location, null);
        if (convertView != null) {
            TextView _TextView2 = (TextView) convertView.findViewById(R.id.tv_location);
            _TextView2.setText(mList.get(position).getSpaceName());
        }
        return convertView;
    }
}